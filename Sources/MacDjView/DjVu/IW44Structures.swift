// MARK: - Linear bytemap for wavelet coefficients
// Uses Int16 to match DjVu.js Int16Array — wrapping arithmetic is part of the algorithm
final class LinearBytemap {
    let width: Int
    let height: Int
    var data: [Int16]

    init(width: Int, height: Int) {
        self.width = width
        self.height = height
        self.data = [Int16](repeating: 0, count: width * height)
    }

    func get(_ row: Int, _ col: Int) -> Int16 {
        data[row * width + col]
    }

    func set(_ row: Int, _ col: Int, _ val: Int16) {
        data[row * width + col] = val
    }

    func add(_ row: Int, _ col: Int, _ val: Int16) {
        data[row * width + col] &+= val
    }

    func sub(_ row: Int, _ col: Int, _ val: Int16) {
        data[row * width + col] &-= val
    }
}

// MARK: - IW44 Block (64 buckets × 16 coefficients = 1024 coefficients per 32×32 block)
// Uses Int16 to match DjVu.js Int16Array — wrapping arithmetic is part of the algorithm
final class IW44Block {
    // Lazy bucket allocation
    private var buckets: [[Int16]?] = Array(repeating: nil, count: 64)

    func getBucket(_ idx: Int) -> [Int16] {
        if let b = buckets[idx] { return b }
        return [Int16](repeating: 0, count: 16)
    }

    func setBucket(_ idx: Int, _ data: [Int16]) {
        buckets[idx] = data
    }

    func getCoef(_ bucket: Int, _ coef: Int) -> Int16 {
        buckets[bucket]?[coef] ?? 0
    }

    func setCoef(_ bucket: Int, _ coef: Int, _ value: Int16) {
        if buckets[bucket] == nil {
            buckets[bucket] = [Int16](repeating: 0, count: 16)
        }
        buckets[bucket]![coef] = value
    }

    var hasBucket: [Bool] {
        (0..<64).map { buckets[$0] != nil }
    }

    func ensureBucket(_ idx: Int) {
        if buckets[idx] == nil {
            buckets[idx] = [Int16](repeating: 0, count: 16)
        }
    }
}
